/* 
    Operadores Lógicos
*/

var x = 6;
alert((x >=1) && (x <= 10));   // true - As 2 instruções sõa verdadeiras
alert((x >=1) || (x <= 10));   //true - Pelo menos uma é verdadeira

alert(!(x >= 1)); // altera o resultado dessa avaliação
// passa a ser x < 1


/*
    Operadores Lógicos

&&    AND (e)    // compara se dois operandos retornam valores verdadeiros
||   OR (ou)      //verifica se um dos dois operandos é verdadeiro
!  NOT (não)    // altera o resultado booleano da operação

*/